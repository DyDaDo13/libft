#include "libft.h"

char	*ft_strcpy(char *s1, char *s2)
{
	int	i;

	i = -1;
	if (!s2)
		return (NULL);
	if (!s1)
		s1 = malloc(sizeof(char) * ft_strlen(s2) + 1);
	while (s2[++i])
		s1[i] = s2[i];
	s1[i] = '\0';
	return (s1);
}
